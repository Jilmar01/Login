    private void typeUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_typeUserMouseClicked
        // TODO add your handling code here:
        if(typeUser.getText().equals("Ingrese su usuario")){
            typeUser.setText("");
            typeUser.setForeground(Color.WHITE);
        }
        if(String.valueOf(typePassword.getPassword()).isEmpty()){
            typePassword.setText("********");
            typePassword.setForeground(Color.gray);
        }
    }//GEN-LAST:event_typeUserMouseClicked

    private void typePasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_typePasswordMouseClicked
        // TODO add your handling code here:
        if(String.valueOf(typePassword.getPassword()).equals("********")){
            typePassword.setText("");
            typePassword.setForeground(Color.WHITE);
        }
        if(typeUser.getText().isEmpty()){
            typeUser.setText("Ingrese su usuario");
            typeUser.setForeground(Color.gray);
        }
    }//GEN-LAST:event_typePasswordMouseClicked

