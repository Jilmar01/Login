    private void iniciarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_iniciarBtnMouseClicked
        // TODO add your handling code here:
        if(typeUser.getText().equals("Ingrese su usuario") || typeUser.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrecta");
        }else if(String.valueOf(typePassword.getPassword()).equals("********") || String.valueOf(typePassword.getPassword()).isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrecta");
        }else{
            javax.swing.JOptionPane.showMessageDialog(this, "Intento de login con los datos:\nUsuario: "+ typeUser.getText() + "\nContraseña: " + String.valueOf(typePassword.getPassword()), "LOGIN",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_iniciarBtnMouseClicked

